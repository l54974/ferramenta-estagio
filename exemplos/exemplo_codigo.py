def saudacao(nome: str, vezes: int) -> str:
    """Repete a saudação 'Olá <nome>' um certo número de vezes."""
    return f"Olá {nome} " * vezes

def multiplicar(a: int, b: int) -> int:
    """Multiplica dois números inteiros."""
    return a * b

def inverter(valor: bool) -> bool:
    """Inverte um valor booleano."""
    return not valor
